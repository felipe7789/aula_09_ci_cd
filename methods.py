def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(x,y):
    return x+y

def subtr(x,y):
    return x-y

def multi(x,y):
    return x*y

def divi(x,y):
    return x/y